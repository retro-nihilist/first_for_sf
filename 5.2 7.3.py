"""У вас есть словарь prices, содержащий цены на литровые упаковки соков в интернет-магазине.
 Вам необходимо применить к этим ценам скидку 5 % и округлить полученное значение до двух 
 знаков после запятой. Новый словарь положите в переменную new_prices. В решении используйте
   функцию map()."""











prices = {'яблоко': 99, 'апельсин': 99, 'вишня': 147, 'персик': 145, 'грейпфрут': 139}  
