prices = {'Штаны': 2100, 'Кофта': 1520, 'Свитер': 3520, 'Галстук': 780, 'Ремень': 1390}  
## new_prices = {'Штаны': 1995.0, 'Кофта': 1444.0, 'Свитер': 3344.0, 'Галстук': 741.0, 'Ремень': 1320.5}

def persent (x):
    name, text = x
    text = text - text*0.05
    return (name, text)


    

new_prices = dict(map(persent, prices.items()))

print(new_prices)

